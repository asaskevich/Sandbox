#include <iostream>

using namespace std;

int main() {
	bool world_is_sphere = true;
	// IF-ELSE statement
	if (world_is_sphere) cout << "All right!" << endl;
	else cout << "Are you prehistoric man?" << endl;

	int i = 100;
	// IF statement
	if (i <= 100) cout << "Yes, i <= 100" << endl;

	// IF-ELSEIF-ELSE statement
	int k = 1;
	if (k == 1) cout << "K == 1" << endl;
	else if (k == 2) cout << "K == 2" << endl;
	else cout << "K == 3" << endl;

	return 0;
}
