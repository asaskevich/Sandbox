public class Example6 {
	public static void main(String[] args) {
		int i = 4;
		// Simple IF-ELSEIF-ELSE statement
		// You can also use IF or IF-ELSE or IF-ELSEIF
		if (i < 4) System.out.println("i < 4");
		else if (i == 4) System.out.println("i == 4");
		else if (i >= 4) System.out.println("i >= 4");
		else System.out.println("Another case");
	}
}
